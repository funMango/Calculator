import Foundation

enum Operator : String, Equatable {
    case addition = "+"
    case subtraction = "-"
    case multiplication = "*"
    case division = "/"
    case reset = "C"
    case percent = "%"
    case changeSign = "+/-"
    case result = "="        
}
